number = int(input("Enter a  number: "))

if number == 1:
    print("\nMonday")
elif number == 2:
    print("\nTuesday")
elif number == 3:
    print("\nWednesday")
elif number == 4:
    print("\nThursday")
elif number == 5:
    print("\nFriday")
elif number == 6:
    print("\nSaturday")
elif number == 7:
    print("\nSunday")
else:
    print(f"\n{number} is outside the range of 1 through 7.")